// Convert string to number
let stringToNumber = "55.5";
stringToNumber = parseInt(stringToNumber);
console.log(stringToNumber);

// Convert number to string
let numberToString = 55;
numberToString = numberToString.toString();
console.log(numberToString);

// Convert string to decimal
let stringToDecimal = "55.5";
stringToDecimal = parseFloat(stringToDecimal);
console.log(stringToDecimal);

// Convert number to boolean
let numberToBoolean = 55;
numberToBoolean = Boolean(numberToBoolean);
console.log(numberToBoolean);
